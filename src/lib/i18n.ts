/**
 * useTranslation — returns a function `t(key)` that resolves to the current
 * language's string, or falls back to the English string, or the raw key.
 *
 * Usage:
 *   const t = useTranslation();
 *   <h1>{t("dashboard")}</h1>
 */
export function useTranslation(): (key: string, fallback?: string) => string {
  // Import useBrandFlowStore dynamically to avoid circular dependency
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { useBrandFlowStore } = require("@/store/brandflow-store");
  const lang = useBrandFlowStore((s: any) => s.language) || "en";
  return (key: string, fallback?: string) => {
    return translations[lang]?.[key] || translations.en[key] || fallback || key;
  };
}

/**
 * t() — standalone function (not a hook) that takes a language explicitly.
 */
export function t(lang: Language, key: string, fallback?: string): string {
  return translations[lang]?.[key] || translations.en[key] || fallback || key;
}

export default translations;
